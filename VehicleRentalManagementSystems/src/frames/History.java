    private void loadRentalHistory() {
        DefaultTableModel model = (DefaultTableModel) historytable.getModel();
        model.setRowCount(0);

        try (Connection con = getConnection()) {
            String sql = "SELECT Client_Name, Plate_No, Rental_Days, Total FROM rentals WHERE Client_Name = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, MainFrame.name);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[] {
                    rs.getString("Client_Name"),
                    rs.getString("Plate_No"),
                    rs.getInt("Rental_Days"),
                    rs.getDouble("Total")
                });
            }

            historytable.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to load history: " + e.getMessage());
        }
    }


   
